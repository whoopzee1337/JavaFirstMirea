package ru.mirea.lab2;

public class Head {
    public int weightOfHead;
    public int radius;
    public Head(int weight , int radius){
        this.weightOfHead = weight;
        this.radius = radius;
    }
    public int getWeightOfHead(){
        return weightOfHead;
    }

    public int getRadius() {
        return radius;
    }
}
